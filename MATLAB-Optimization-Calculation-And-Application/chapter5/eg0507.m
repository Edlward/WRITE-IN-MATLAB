%目标函数所对应的设计变量的系数，为求极小，故取原目标函数系数的相反数
c=[-5;-8];
%不等式约束
A=[1 1;5 9];
b=[6;45];
%设计变量的边界约束，无上界约束
lb=[0;0];
%均要求为整数变量
M=[1;2];
%判断是否整数的误差限
Tol=1e-8;
%求解原问题松弛线性规划的最优解x和在x处的目标函数值
[x,fval]=linprog(c,A,b,[],[],lb,[])
%求最优解整数解x1和目标函数在x1处的值，结果为原问题最优值的相反数
[x1,fval1]=intprog(c,A,b,[],[],lb,[],M,Tol)
